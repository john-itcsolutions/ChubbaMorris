ECode CFinance::CompareTo(
    /* [in] */ IInterface* another,
    /* [out] */ Int32* result)
{
    VALIDATE_NOT_NULL(result);

    ICFinance* oa = ICH::Probe(another);
    if (oa == NULL) {
        *result = 1;
        return E_CLASS_CAST_EXCEPTION;
    }

    CFinance* a = (CFinance*)oa;
    *result = mName.Compare(a->mName);
    return NOERROR;
}

